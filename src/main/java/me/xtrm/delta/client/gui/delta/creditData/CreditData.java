package me.xtrm.delta.client.gui.delta.creditData;

public class CreditData {
	
	public String name;
	public String uuid;
	public String role;
	public String moreInfo;
	
	public CreditData(String name, String uuid, String role, String moreInfo) {
		this.name = name;
		this.uuid = uuid;
		this.role = role;
		this.moreInfo = moreInfo;
	}

}
